CREATE TABLE ORGANIZATOR(
    id_organizator NUMBER PRIMARY KEY,
    nume VARCHAR2(100),
    rol VARCHAR2(100),
    nrTelefon NUMBER NOT NULL
);

DROP TABLE intervalorar;

CREATE TABLE CLUBDANS(
    id_clubDans NUMBER PRIMARY KEY,
    id_organizator NUMBER,
    nume VARCHAR2(100),
    fondator VARCHAR2(100),
    nrTelefon NUMBER NOT NULL,
    FOREIGN KEY (id_organizator) REFERENCES ORGANIZATOR(id_organizator) ON DELETE SET NULL
);

CREATE TABLE CATEGORII(
    id_categorie NUMBER PRIMARY KEY,
    clasa VARCHAR2(100),
    varsta VARCHAR(100),
    stil VARCHAR(100)
);

CREATE TABLE PERECHI(
    id_pereche NUMBER PRIMARY KEY,
    id_clubDans NUMBER,
    id_categorie NUMBER,
    nume_fata VARCHAR2(100),
    nume_baiat VARCHAR2(100),
    clasificare NUMBER,
    punctaj NUMBER NOT NULL,
    FOREIGN KEY (id_clubDans) REFERENCES CLUBDANS(id_clubDans) ON DELETE SET NULL,
    FOREIGN KEY (id_categorie) REFERENCES CATEGORII(id_categorie) ON DELETE SET NULL
);

CREATE TABLE ARBITRI(
    id_arbitru NUMBER PRIMARY KEY,
    nume VARCHAR2(100),
    nrTelefon NUMBER NOT NULL
);

CREATE TABLE PUNCTAJE(
    id_punctaj NUMBER PRIMARY KEY,
    id_arbitru NUMBER,
    id_pereche NUMBER,
    nota NUMBER NOT NULL,
    FOREIGN KEY (id_arbitru) REFERENCES ARBITRI(id_arbitru) ON DELETE SET NULL,
    FOREIGN KEY (id_pereche) REFERENCES PERECHI(id_pereche) ON DELETE SET NULL
);


CREATE TABLE INTERVALEORARE(
    id_intervalOrar NUMBER PRIMARY KEY,
    id_categorie NUMBER,
    zi DATE,
    interval_inceput NUMBER,
    etapa VARCHAR2(100),
    FOREIGN KEY (id_categorie) REFERENCES CATEGORII(id_categorie) ON DELETE SET NULL
);

CREATE TABLE PREMIU(
    id_premiu NUMBER PRIMARY KEY,
    id_categorie NUMBER,
    premiu VARCHAR(100),
    FOREIGN KEY (id_categorie) REFERENCES CATEGORII(id_categorie) ON DELETE SET NULL
);

CREATE TABLE ANTRENOR(
    id_antrenor NUMBER PRIMARY KEY,
    id_clubDans NUMBER,
    nume VARCHAR(100),
    nrTelefon NUMBER NOT NULL,
    FOREIGN KEY (id_clubDans) REFERENCES CLUBDANS(id_clubDans) ON DELETE SET NULL
);

CREATE TABLE SPONSOR(
    id_sponsor NUMBER PRIMARY KEY,
    nume VARCHAR2(100),
    persoanaContact VARCHAR(100),
    nrTelefon NUMBER NOT NULL
);

CREATE TABLE CONTRACT(
    id_organizator NUMBER,
    id_sponsor NUMBER,
    data_semnare DATE,
    scop VARCHAR2(100),
    PRIMARY KEY (id_organizator, id_sponsor),
    FOREIGN KEY (id_organizator) REFERENCES ORGANIZATOR(id_organizator) ON DELETE SET NULL,
    FOREIGN KEY (id_sponsor) REFERENCES SPONSOR(id_sponsor) ON DELETE SET NULL
);

